class UserInfo{
  final String userName;
  final String userId;
  final String userBio;
  final String userProfile;
  final String title;
  final bool isPrivate;

  UserInfo({
    required this.userName,
    required this.userId,
    required this.userBio,
    required this.userProfile,
    required this.title,
    required this.isPrivate,
  });
}